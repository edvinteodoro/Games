    private void newGame() {
        //JOptionPane.showInputDialog(this);
        String vidas = String.valueOf(cantidadVidas);
        vidasDisponibles.setText(vidas);
        String palabraNueva = palabraSecreta.getText();
        String pista1 = pista1TextField.getText();
        String pista2 = pista2Texfield.getText();
        String pista3 = pista3Textfield.getText();
        agregarPista(pista1);
        agregarPista(pista2);
        agregarPista(pista3);
        for (int i = 0; i < palabraNueva.length(); i++) {
            char letra = palabraNueva.charAt(i);
            String s = String.valueOf(letra);
            letrasDePalabra.add(s);
            System.out.println(letrasDePalabra.get(i));
        }
        for (int i = 0; i < palabraNueva.length(); i++) {
            letrasEnPantalla.add(" __");
        }

        procesoPalabra.setText(mostrarPalabra());
        habilitarTodo();
        vidasDisponibles.setText(Integer.toString(cantidadVidas));
    }

    private void habilitarTodo() {
        letraIngresada.setEditable(true);
        verPista.setEnabled(true);
        intentar.setEnabled(true);
    }

    public void obtenerPalabraPorLetras(String textoEntrada) {
        for (int i = 0; i < textoEntrada.length(); i++) {
            char letra = textoEntrada.charAt(i);
            String s = String.valueOf(letra);
            letrasDePalabra.add(s);
        }
    }

    public boolean letraExisteEnPalabra(String letra) {
        for (int i = 0; i < letrasDePalabra.size(); i++) {
            if (letra.equalsIgnoreCase(letrasDePalabra.get(i))) {
                return true;
            } else {
            }
        }
        return false;
    }

    public void crearPalabra() {
        for (int i = 0; i < letrasDePalabra.size(); i++) {
            letrasEnPantalla.add("__ ");
        }
    }

    public String mostrarPalabra() {
        String letraString = "";
        for (int i = 0; i < letrasEnPantalla.size(); i++) {
            letraString += String.valueOf(letrasEnPantalla.get(i));
        }
        return letraString;
    }

    public void indexLetraACambiar(String letra) {
        for (int i = 0; i < letrasDePalabra.size(); i++) {
            if (letra.equalsIgnoreCase(letrasDePalabra.get(i))) {
                letrasEnPantalla.set(i, letra);
            }
        }
    }

    public void mostrarPista() {
        int numPista = cantidadPistasUsadas + 1;
        JOptionPane.showMessageDialog(null, "Pista #" + numPista + ": " + pistas.get(cantidadPistasUsadas));
        cantidadPistasUsadas++;
    }

    public void mostrarLetrasUsadas() {
        for (int i = 0; i < letrasUsadas.size(); i++) {
            System.out.println("Usada: " + letrasUsadas.get(i));
        }
    }

    public void agregarPista(String pista) {
        pistas.add(pista);
    }

    public boolean verificarLetraUsada(String letra) {
        for (int i = 0; i < letrasUsadas.size(); i++) {
            if (letra.equalsIgnoreCase(letrasUsadas.get(i))) {
                return true;
            }
        }
        return false;
    }

    public boolean yaGanoONo() {
        if (cantidadAciertos == letrasDePalabra.size()-1) {
            return true;
        }
        return false;
    }

    public void resetear() {
        estadoLabel.setIcon(icon0);
        procesoPalabra.setText("");
        letrasDePalabra.clear();
        letrasEnPantalla.clear();
        letrasUsadas.clear();
        cantidadVidas = 7;
        cantidadAciertos = 0;
        palabraSecreta.setText("");
        pista1TextField.setText("");
        pista2Texfield.setText("");
        pista3Textfield.setText("");
        letrasUsadasTextArea.setText("");
        iniciarButton.setEnabled(true);
        palabraSecreta.setEditable(true);
        pista1TextField.setEditable(true);
        pista2Texfield.setEditable(true);
        pista3Textfield.setEditable(true);
        verPista.setEnabled(false);
        letraIngresada.setEditable(false);
        intentar.setEnabled(false);
        reiniciarButtom.setEnabled(false);
    }

